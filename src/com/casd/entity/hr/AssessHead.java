package com.casd.entity.hr;

import java.io.Serializable;
import java.sql.Date;

/**
*  评估表头 assess_head_table
* */
public class AssessHead implements Serializable{

	private int assess_head_id; // 评估考核id
	private String assess_head_name; // 姓名
	private String assess_head_position; // 职位
	private String assess_head_department; // 部门
	private String assess_head_company; // 公司
	private int assess_head_totalscore; // 总分
	private String assess_head_evaluator; // 评估人
	private Date assess_head_creatdate; // 评估人

	public int getAssess_head_id() {
		return assess_head_id;
	}

	public void setAssess_head_id(int assess_head_id) {
		this.assess_head_id = assess_head_id;
	}

	public String getAssess_head_name() {
		return assess_head_name;
	}

	public void setAssess_head_name(String assess_head_name) {
		this.assess_head_name = assess_head_name;
	}


	public String getAssess_head_department() {
		return assess_head_department;
	}

	public void setAssess_head_department(String assess_head_department) {
		this.assess_head_department = assess_head_department;
	}


	public String getAssess_head_evaluator() {
		return assess_head_evaluator;
	}

	public void setAssess_head_evaluator(String assess_head_evaluator) {
		this.assess_head_evaluator = assess_head_evaluator;
	}

	public Date getAssess_head_creatdate() {
		return assess_head_creatdate;
	}

	public void setAssess_head_creatdate(Date assess_head_creatdate) {
		this.assess_head_creatdate = assess_head_creatdate;
	}

	public String getAssess_head_position() {
		return assess_head_position;
	}

	public void setAssess_head_position(String assess_head_position) {
		this.assess_head_position = assess_head_position;
	}

	public String getAssess_head_company() {
		return assess_head_company;
	}

	public void setAssess_head_company(String assess_head_company) {
		this.assess_head_company = assess_head_company;
	}

	public int getAssess_head_totalscore() {
		return assess_head_totalscore;
	}

	public void setAssess_head_totalscore(int assess_head_totalscore) {
		this.assess_head_totalscore = assess_head_totalscore;
	}

}

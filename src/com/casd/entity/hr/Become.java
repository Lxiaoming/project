package com.casd.entity.hr;

import java.io.Serializable;


/**
 *  uc_become表
 * */
public class Become implements Serializable{
	
	private int bc_id;    //主键编号
	private int user_id;  //转正人编号
	
	private String username; //转正人姓名
	
	private String role_name;//转正人职位
	
	private int bc_status;//转正状态
	
	private String on_trial;//试用期待遇
	
	private String incorporation_date;//进入公司时间
	
	private String close_time;//试用期结束时间
	
	private String bc_personal;//个人评价
	
	private String bc_department; //部门 

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}


	public int getBc_status() {
		return bc_status;
	}

	public void setBc_status(int bc_status) {
		this.bc_status = bc_status;
	}


	public int getBc_id() {
		return bc_id;
	}

	public void setBc_id(int bc_id) {
		this.bc_id = bc_id;
	}

	public String getIncorporation_date() {
		return incorporation_date;
	}

	public void setIncorporation_date(String incorporation_date) {
		this.incorporation_date = incorporation_date;
	}

	public String getClose_time() {
		return close_time;
	}

	public void setClose_time(String close_time) {
		this.close_time = close_time;
	}

	public String getOn_trial() {
		return on_trial;
	}

	public void setOn_trial(String on_trial) {
		this.on_trial = on_trial;
	}

	public String getBc_personal() {
		return bc_personal;
	}

	public void setBc_personal(String bc_personal) {
		this.bc_personal = bc_personal;
	}

	public String getBc_department() {
		return bc_department;
	}

	public void setBc_department(String bc_department) {
		this.bc_department = bc_department;
	}

	public String getRole_name() {
		return role_name;
	}

	public void setRole_name(String role_name) {
		this.role_name = role_name;
	}


	
}

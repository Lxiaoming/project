package com.casd.entity.flow;

import java.io.Serializable;
import java.util.List;

public class Flow implements Serializable {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer flow_id; 				//流程id 
	private String flow_name;			//流程名
	private String flow_description;	//说明
	private String flow_auditURL;		//流程审核界面
	public Integer getFlow_id() {
		return flow_id;
	}
	public void setFlow_id(Integer flow_id) {
		this.flow_id = flow_id;
	}
	public String getFlow_name() {
		return flow_name;
	}
	public void setFlow_name(String flow_name) {
		this.flow_name = flow_name;
	}
	public String getFlow_description() {
		return flow_description;
	}
	public void setFlow_description(String flow_description) {
		this.flow_description = flow_description;
	}
	public String getFlow_auditURL() {
		return flow_auditURL;
	}
	public void setFlow_auditURL(String flow_auditURL) {
		this.flow_auditURL = flow_auditURL;
	}
    
	
}

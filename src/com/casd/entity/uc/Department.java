package com.casd.entity.uc;

import java.io.Serializable;

public class Department implements Serializable{


	/**
	 * 
	 */
	private static final long serialVersionUID = -3962653241287108418L;
	/**
	 * 所属部门
	 * 
	 * */
		
		private int department_id; // 部门编号
		private String department_name; // 部门名称
		private int department_centerId; // 中心编号
		private int department_companyId; // 公司编号
		public int getDepartment_id() {
			return department_id;
		}
		public void setDepartment_id(int department_id) {
			this.department_id = department_id;
		}
		public String getDepartment_name() {
			return department_name;
		}
		public void setDepartment_name(String department_name) {
			this.department_name = department_name;
		}
		public int getDepartment_centerId() {
			return department_centerId;
		}
		public void setDepartment_centerId(int department_centerId) {
			this.department_centerId = department_centerId;
		}
		public int getDepartment_companyId() {
			return department_companyId;
		}
		public void setDepartment_companyId(int department_companyId) {
			this.department_companyId = department_companyId;
		}
		
		


}
